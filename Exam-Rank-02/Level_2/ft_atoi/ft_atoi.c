#include <unistd.h>
#include <stdio.h>

int	ft_atoi(const char *str)
{
	int	output;
	int	sign;
	int	i;

	i = 0;
	sign = 1;
	while (str[i] == '\t' || str[i] == '\n' || str[i] == '\v' || str[i] == '\f' || str[i] == '\r' || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
		output = output * 10 + (str[i++] - '0');
	return (output * sign);
}

int main()
{
	printf("%d", ft_atoi("        20000000"));
	return (0);
}




